fn main() {
    println!("Numeric operations");

    // addition
    let sum = 5 + 10;
    println!("\t5 + 10 = {}", sum);

    // subtraction
    let difference = 95.5 - 4.3;
    println!("\t95.5 - 4.3 = {}", difference);

    // multiplication
    let product = 4 * 30;
    println!("\t4 * 30 = {}", product);

    // division
    let quotient = 56.7 / 32.2;
    println!("\t56.7 / 32.2 = {}", quotient);

    // remainder
    let remainder = 43 % 5;
    println!("\t43 % 5 = {}", remainder);
}
